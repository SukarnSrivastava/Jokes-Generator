  let categories = document.getElementsByClassName("categories")[0];
  let selectedButton = null; // Track the previously selected button
  
  categories.addEventListener("click", e => {
    if (selectedButton) {
      selectedButton.style.backgroundColor = ""; // Unset the color of the previously selected button
      selectedButton.style.color = "";
    }
  
    randomJokesGenerator(e.target.id);
    storingCat = e.target.id;
    catButton(e.target); // Pass the entire target element to the catButton function
  });
  
  function catButton(button) {
    button.style.backgroundColor = "#7a80f5"; // Set the color of the clicked button to red
    button.style.color = "white";
    selectedButton = button; // Update the selectedButton variable to track the current selection
  }
  
  
  
  function randomJokesGenerator(jokes) {
    let jokesData;
    if (jokes === "pjokes") {
      jokesData = programmingJokes;
    }
    else if (jokes === "ajokes") {
      jokesData = adultJokes;
    }
    else if (jokes === "hjokes") {
      jokesData = humourJokes;
    }
    else {
      let randomNum = Math.floor(Math.random() * 2);
      let cat = [adultJokes, programmingJokes, humourJokes];
      jokesData = cat[randomNum];
    }
  
    let randomNum = Math.floor(Math.random() * (jokesData.length - 1));
    let randomJokes = jokesData[randomNum];
    document.getElementById("joke").innerHTML = randomJokes;
  }
  
  let a = document.getElementById("btn");
  a.addEventListener(("click"), e => randomJokesGenerator(storingCat));
  
  